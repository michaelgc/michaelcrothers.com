<script type="text/javascript">
$(document).ready(function () {

	$('.navbar').affix()

	$('.carousel').carousel({
		interval: 5000
	});

	$('.carousel').bind('slide', function(){
		$('.carousel img').hide();
		$('.carousel .loading').show();
		setInterval(function() {
			$('.carousel img').show();
			$('.carousel .loading').hide();
		}, 2500);

	});

	$('#message').on('hidden', function() {
		$(this).data('modal').$element.removeData();
	});

	var $root = $('html, body');

	$('a.scrollTo').click(function() {
		var href = $.attr(this, 'href');
		$root.animate({
			scrollTop: $(href).offset().top
		}, 500, function () {
			window.location.hash = href;
		});
		return false;
	});


	$("#contact-form").validate({

		rules: {
			inputFullName: {
				required: true,
				minlength: 5
			},
			inputContactNumber: {
				required: true,
				minlength: 5
			},
			inputEmailAddress: {
				required: true,
				email: true
			},
			inputEnquiry: {
				required: true
			}

		},

		messages: { // specify custom messages
			inputFullName: {
				required: 'This field is required',
				minlength: 'Minimum length: 5'
			},
			inputContactNumber: {
				required: 'This field is required'
			},
			inputEmailAddress: 'Invalid e-mail address',
			message: {
				required: 'This field is required'
			},
			inputEnquiry: {
				required: 'This field is required',
			}
		},


		submitHandler: function(form) {
			$.ajax({
				type: "POST",
				url: "contact-form-processing.php",
				data: $("#contact-form").serialize(),
				success: function(response){
					$("#contact-form-container").modal('toggle')
					$("#message").modal('show');
					$("#message .modal-body").append(response);
				}
			});

			return false;
		}

	});

});
</script>

